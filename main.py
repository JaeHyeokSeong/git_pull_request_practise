def add(a, b):
    return a + b

def multiply(a, b):
    return a * b

def subtract(a, b):
    return a - b

if __name__ == '__main__':
    result = add(1, 2)
    print(f'result: {result}')
    result = multiply(2, 2)
    print(f'result: {result}')
    result = subtract(3, 1)
    print(f'result: {result}')
